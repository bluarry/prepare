typedef long long ll;

class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        int R=obstacleGrid.size(),C=obstacleGrid[0].size();
        vector<vector<ll> > dp(R,vector<ll>(C,0));
        for(int i=0;i<R;i++){
            for(int j=0;j<C;j++){
                dp[i][j]=obstacleGrid[i][j];
            }
        }
        if(dp[0][0]==1){
        	return 0;
		}
        dp[0][0]=1;
		for(int i=1;i<R;i++){
			dp[i][0]= (dp[i-1][0]==1&& dp[i][0]==0)?1:0;
		}
        
		for(int i=1;i<C;i++){
			dp[0][i]= (dp[0][i-1]==1 && 0==dp[0][i])?1:0;
		}

        for(int i=1;i<R;i++){
            for(int j=1;j<C;j++){
                if(dp[i][j]==1){
                    dp[i][j]=0; 
                }else{
                    dp[i][j]=dp[i-1][j]+dp[i][j-1];
                }
            }
        }
        return static_cast<int>(dp[R-1][C-1]);
    }
};

